name = ''

# While Statement
while name != 'your name':
    print('Please type your name.')
    
    name = input()
    
print('Thank you!')

# while with if Statement
while True :
    print('Please type your name.')
    name = input()
    if name == 'your name':
        break
        
print('Thank you!')

# infinite While Loop
while True :
    print('Hello World...')